    private void btnSearchStudioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchStudioActionPerformed
        if (txtName.getText() != null && !"".equals(txtName.getText())) {
            if (studioDAO.getStudio(txtName.getText()) == null) {
                Studio studio = new Studio();
                studio.setName(txtName.getText());

                if (JOptionPane.showConfirmDialog(this, "Štúdio " + txtName.getText() + " neexistuje. Chcete ho pridať do zoznamu?", "???", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    studioDAO.addStudio(studio);

                    loadStudioData((int) studio.getID());
                    this.studioID = (int) studio.getID();
                    lblWarnStudio.setForeground(Color.green);
                    lblWarnStudio.setText("Štúdio bolo úspešne pridané");                    
                }
            } else {
                Studio studio = studioDAO.getStudio(txtName.getText());
                loadStudioData((int) studio.getID());
                this.studioID = (int) studio.getID();
            }
        }
    }//GEN-LAST:event_btnSearchStudioActionPerformed

