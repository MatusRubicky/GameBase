    private void btnAddGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddGameActionPerformed
        Game game = (Game) cbxListOfGames.getSelectedItem();
        if (game == null) {
            lblWarnGame.setText("Vyberte hru");
            return;
        }
        if (studioDAO.checkGame(studioID, game.getID())) {
            lblWarnGame.setForeground(Color.red);
            lblWarnGame.setText("Hra už bola pridaná!!");
            return;
        }

        studioDAO.addGameToStudio(studioID, game.getID());
        loadStudioData(studioID);
        lblWarnGame.setForeground(Color.green);
        lblWarnGame.setText("Hra bola úspešne priradená");
    }//GEN-LAST:event_btnAddGameActionPerformed

